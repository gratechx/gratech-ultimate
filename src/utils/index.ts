/**
 * Convert Western Arabic numerals to Eastern Arabic
 */
export function toArabicNumerals(num: number | string): string {
  const arabicNumerals = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];
  return String(num).replace(/[0-9]/g, digit => arabicNumerals[parseInt(digit)]);
}

/**
 * Convert Eastern Arabic numerals to Western
 */
export function toWesternNumerals(str: string): string {
  const arabicNumerals = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];
  return str.replace(/[٠-٩]/g, digit => String(arabicNumerals.indexOf(digit)));
}

/**
 * Check if text is RTL (Arabic/Hebrew)
 */
export function isRtlText(text: string): boolean {
  const rtlRegex = /[\u0591-\u07FF\uFB1D-\uFDFD\uFE70-\uFEFC]/;
  return rtlRegex.test(text);
}
